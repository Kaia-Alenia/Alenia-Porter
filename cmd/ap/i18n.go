var currentLang = initLanguage()

func initLanguage() string {
	for _, raw := range []string{os.Getenv("LC_ALL"), os.Getenv("LC_MESSAGES"), os.Getenv("LANG")} {
		if code := normalizeLanguageCode(raw); code != "" {
			if _, ok := translations[code]; ok {
				return code
			}
		}
	}
	return "es"
}

func normalizeLanguageCode(raw string) string {
	raw = strings.TrimSpace(strings.ToLower(raw))
	if raw == "" {
		return ""
	}
	raw = strings.ReplaceAll(raw, "_", "-")
	raw = strings.SplitN(raw, ".", 2)[0]
	raw = strings.SplitN(raw, "@", 2)[0]
	if raw == "pt-br" || strings.HasPrefix(raw, "pt-br-") {
		return "pt-br"
	}
	if idx := strings.IndexByte(raw, '-'); idx > 0 {
		raw = raw[:idx]
	}
	return raw
}

func applyLanguage(code string) bool {
	code = normalizeLanguageCode(code)
	if code == "" {
		return false
	}
	if _, ok := translations[code]; ok {
		currentLang = code
		return true
	}
	return false
}

func T(key string) string {
	if val, ok := translations[currentLang][key]; ok {
		return val
	}
	if val, ok := translations["en"][key]; ok {
		return val
	}
	return key
}
